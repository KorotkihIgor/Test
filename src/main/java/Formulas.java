public class Formulas {

    public double squareCircle(double r) {
        return 3.14 * (r * r);
    }

    public int squareArea(int a) {
        return a * a;
    }

    public int squareRectangle(int a, int b) {
        return a * b;
    }

    public double squareThreeCornered(double a, double b) {
        return (a * b) / 2;
    }
}
